// Manipulacao Numero

let numero = 3.60

// Arredondamento
Math.ceil(numero) // Para cima
Math.floor(numero) // Para Baixo
Math.round(numero) // Mias Próximo

// Gerar numeros aleatorios
Math.random() // 0 - 1
Math.random() * 100 // de 0 - ao múltiplicador

// Comparando
Math.min(5, 10)  // o menor dos dois parametros
Math.max(5, 10) // o maior dos dois parametros

// Potencia e Raiz Quadrada
Math.pow(2,5) // 1 - base; 2 - expoente
Math.sqrt(7) // Raix quadrada do numero
